    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
   try {
        String oldLrn = txtLrn2.getText().trim();
        String name = txtName.getText().trim();
        String gender = radioMale.isSelected() ? "Male" : radioFemale.isSelected() ? "Female" : "";
        String grade = txtGrade.getText().trim();
        String section = txtSection.getText().trim();
        String strand = strandCombobox.getSelectedItem().toString();
        String heightStr = txtHeight.getText().trim();
        String weightStr = txtWeight.getText().trim();

        // Validate fields
        if (oldLrn.isEmpty() || !oldLrn.matches("\\d{12}")) {
            JOptionPane.showMessageDialog(null, "Invalid LRN. Must be 12 digits.", "Invalid", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (name.isEmpty() || gender.isEmpty() || grade.isEmpty() || section.isEmpty() ||
            strand.isEmpty() || heightStr.isEmpty() || weightStr.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please fill in all fields.", "Missing Data", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (!heightStr.matches("\\d+(\\.\\d+)?") || !weightStr.matches("\\d+(\\.\\d+)?")) {
            JOptionPane.showMessageDialog(null, "Height and Weight must be numeric.", "Invalid Input", JOptionPane.ERROR_MESSAGE);
            return;
        }

        double height = Double.parseDouble(heightStr);
        double weight = Double.parseDouble(weightStr);
        double bmi = weight / Math.pow(height / 100, 2);

        String classification;
        if (bmi < 16) classification = "Severe Thinness";
        else if (bmi < 17) classification = "Moderate Thinness";
        else if (bmi < 18.5) classification = "Mild Thinness";
        else if (bmi < 25) classification = "Normal";
        else if (bmi < 30) classification = "Overweight";
        else if (bmi < 35) classification = "Obese Class I";
        else if (bmi < 40) classification = "Obese Class II";
        else classification = "Obese Class III";

        Connection connection = ConnectionProvider.getCon();
        PreparedStatement ps = connection.prepareStatement(
            "UPDATE student SET name=?, gender=?, grade=?, section=?, strand=?, height=?, weight=?, classification=? WHERE id=?"
        );
        ps.setString(1, name);
        ps.setString(2, gender);
        ps.setString(3, grade);
        ps.setString(4, section);
        ps.setString(5, strand);
        ps.setDouble(6, height);
        ps.setDouble(7, weight);
        ps.setString(8, classification);
        ps.setString(9, oldLrn);

        int result = ps.executeUpdate();
        if (result > 0) {
            JOptionPane.showMessageDialog(null, "Student information updated successfully. ");
            clearForm();
        } else {
            JOptionPane.showMessageDialog(null, "No student found with LRN: " + oldLrn, "Not Found", JOptionPane.WARNING_MESSAGE);
        }

        ps.close();
    } catch (Exception ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(null, "Update failed: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnUpdateActionPerformed

