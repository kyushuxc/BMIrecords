    private void searchBtn2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtn2ActionPerformed
    String lrnText = txtLrn2.getText().trim();

    if (lrnText.isEmpty() || !lrnText.matches("\\d{12}")) {
        JOptionPane.showMessageDialog(null, "Invalid LRN. Must be exactly 12 digits.", "Invalid", JOptionPane.ERROR_MESSAGE);
        return;
    }

    try {
        long lrn = Long.parseLong(lrnText);

        Connection con = ConnectionProvider.getCon();
        PreparedStatement ps = con.prepareStatement("SELECT * FROM student WHERE id = ?");
        ps.setLong(1, lrn);
        ResultSet rs = ps.executeQuery();

        if (rs.next()) {
            txtName.setText(rs.getString("name"));
            txtGrade.setText(rs.getString("grade"));
            txtSection.setText(rs.getString("section"));
            strandCombobox.setSelectedItem(rs.getString("strand"));
            txtHeight.setText(String.valueOf(rs.getDouble("height")));
            txtWeight.setText(String.valueOf(rs.getDouble("weight")));

            String gender = rs.getString("gender");
            radioMale.setSelected("Male".equalsIgnoreCase(gender));
            radioFemale.setSelected("Female".equalsIgnoreCase(gender));
        } else {
            JOptionPane.showMessageDialog(null, "No student found with LRN: " + lrn, "Not Found", JOptionPane.WARNING_MESSAGE);
        }

        rs.close();
        ps.close();
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(null, "LRN is too large or invalid.", "Error", JOptionPane.ERROR_MESSAGE);
    } catch (Exception ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(null, "Database error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_searchBtn2ActionPerformed

