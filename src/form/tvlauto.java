  private void fetchUserByGrade(String grade) throws Exception {
    DefaultTableModel model = (DefaultTableModel) studentTable.getModel();
    model.setRowCount(0);
    try {
        Connection con = ConnectionProvider.getCon();
        PreparedStatement ps = con.prepareStatement(
            "SELECT * FROM student WHERE grade = ? AND strand = 'TVL-AUTO'"
        );
        ps.setString(1, grade); 
        
        ResultSet rs = ps.executeQuery();
        while (rs.next()) {
            model.addRow(new Object[] {
                rs.getString("id"),
                rs.getString("name"),
                rs.getString("gender"),
                rs.getString("grade"),
                rs.getString("section"),
                rs.getString("strand"),
                rs.getString("classification")
            });
        }
        txtSearch.setText(""); // Clear search bar
        rs.close();
        ps.close();
    } catch (Exception ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error loading Grade " + grade + "TVL-AUTO students.", "Error", JOptionPane.ERROR_MESSAGE);
    }
}
    private void fetchUser(String searchText) throws Exception {
    filterTable(searchText);
}

private void filterTable(String searchText) {
    DefaultTableModel model = (DefaultTableModel) studentTable.getModel();
    model.setRowCount(0);
    try {
        Connection con = ConnectionProvider.getCon();
        PreparedStatement ps;

        if (searchText == null || searchText.trim().isEmpty()) {
            ps = con.prepareStatement("SELECT * FROM student WHERE strand = 'TVL-AUTO'");
        } else {
            ps = con.prepareStatement(
                "SELECT * FROM student WHERE strand = 'TVL-AUTO' AND " +
                "(name LIKE ? OR id LIKE ? OR grade LIKE ? OR section LIKE ? OR classification LIKE ?)"
            );
            for (int i = 1; i <= 5; i++) {
                ps.setString(i, "%" + searchText + "%");
            }
        }

        ResultSet rs = ps.executeQuery();
        while (rs.next()) {
            model.addRow(new Object[] {
                rs.getString("id"),
                rs.getString("name"),
                rs.getString("gender"),
                rs.getString("grade"),
                rs.getString("section"),
                rs.getString("strand"),
                rs.getString("classification")
            });
        }
        rs.close();
        ps.close();
    } catch (Exception ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error loading TVL-AUTO students.", "Error", JOptionPane.ERROR_MESSAGE);
    }
}
